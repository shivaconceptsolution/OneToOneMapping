package scs;

import java.util.Set;

public class Vendor {
 private int vid;
 private String vname;
 private Set child;
public int getVid() {
	return vid;
}
public void setVid(int vid) {
	this.vid = vid;
}
public String getVname() {
	return vname;
}
public void setVname(String vname) {
	this.vname = vname;
}
public Set getChild() {
	return child;
}
public void setChild(Set child) {
	this.child = child;
}
 
 
}
